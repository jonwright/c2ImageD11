def generate_c2py():
    """Generate the bslz4 section of the .c2py file."""
    lines = []

    nbasic = len(PIXEL_TYPES) * len(ENGINES)
    ncsc = len(PIXEL_TYPES) * len(CSC_TYPES) * len(ENGINES)
    nvariant = len(BACKENDS) * len(ISAS)

    lines.append(C2PY_HEADER.format(
        nbasic=nbasic, ncsc=ncsc, nvariant=nvariant,
        nbackends=len(BACKENDS), nisas=len(ISAS)))

    for engine in ENGINES:
        prefix = engine["prefix"]
        for px in PIXEL_TYPES:
            dtshort = px["dtshort"]
            py_format = px["py_format"]

            # Basic decompress function
            entry_lines = _py_sig_entry_basic(prefix, dtshort, py_format)
            lines.extend(entry_lines)
            variant_lines = _py_sig_variants_basic(prefix, dtshort)
            lines.extend(variant_lines)

    for engine in ENGINES:
        prefix = engine["prefix"]
        for px in PIXEL_TYPES:
            dtshort = px["dtshort"]
            py_format = px["py_format"]
            for csc in CSC_TYPES:
                # CSC function
                entry_lines = _py_sig_entry_csc(prefix, dtshort, csc,
                                                 py_format)
                lines.extend(entry_lines)
                variant_lines = _py_sig_variants_csc(prefix, dtshort, csc)
                lines.extend(variant_lines)

                # CSC1D function
                entry_lines = _py_sig_entry_csc1d(prefix, dtshort, csc,
                                                   py_format)
                lines.extend(entry_lines)
                variant_lines = _py_sig_variants_csc1d(prefix, dtshort, csc)
                lines.extend(variant_lines)

    return "\n".join(lines) + "\n"


