BS_FUNCTIONS_H_HEADER = '''/* Forward declarations for all bitshuffle sparse decompress functions.
 *
 * AUTO-GENERATED by generate_bslz4.py -- DO NOT EDIT BY HAND.
 *
 * {nfuncs} functions = {npixel} pixel types (u8/u16/u32)
 *                   x {nbasic} basic decompress
 *                   + {npixel} pixel types x {ncsc} CSC data types
 *                   x 1 engine (runtime dispatch via encoding param)
 *                   x {nbackends} backends (kcb/bs)
 *                   x {nisas} ISAs (sse42/avx2/avx512)
 *
 * encoding=2 for LZ4, encoding=3 for ZSTD.
 *
 * Used by c2py23 for wrapper codegen.
 */

#ifndef BS_FUNCTIONS_H
#define BS_FUNCTIONS_H

#include <stdint.h>
'''

def generate_bs_functions_h():
    """Generate the complete bs_functions.h content."""
    lines = []

    # Count totals for header comment
    npx = len(PIXEL_TYPES)
    ncsc = len(CSC_TYPES)
    neng = len(ENGINES)
    nbk = len(BACKENDS)
    nisa = len(ISAS)
    nbasic = npx * neng * nbk * nisa
    ncsc_total = npx * ncsc * neng * nbk * nisa
    nfuncs = nbasic + ncsc_total

    lines.append(BS_FUNCTIONS_H_HEADER.format(
        nfuncs=nfuncs, npixel=npx, nbasic="1", ncsc=ncsc,
        nengines=1, nbackends=nbk, nisas=nisa))

    for engine in ENGINES:
        prefix = engine["prefix"]
        eng_suffix = engine["suffix"]

        for backend in BACKENDS:
            bkd_suffix = backend["suffix"]

            lines.append("")
            lines.append("/* ======================================================="
                         "======================")
            lines.append(" * {} engine x {} backend".format(
                eng_suffix.upper(), bkd_suffix.upper()))
            lines.append(" * ======================================================="
                         "======================")

            for isa in ISAS:
                isa_suffix = isa["suffix"]
                for px in PIXEL_TYPES:
                    dtshort = px["dtshort"]
                    datatype = px["datatype"]
                    fn_basic = make_basic_fn_name(prefix, dtshort,
                                                  bkd_suffix, isa_suffix)

                    lines.append("")
                    lines.append("/* {} basic */".format(dtshort))
                    lines.append(
                        "int {fn}(const uint8_t *restrict compressed, "
                        "int compressed_length, "
                        "const uint8_t *restrict mask, int NIJ, "
                        "{dt} *restrict output, uint32_t *restrict output_adr, "
                        "int threshold, int encoding, "
                        "const int64_t *restrict chunk_offsets, "
                        "const int32_t *restrict chunk_lengths, "
                        "int nchunks, "
                        "int32_t *restrict npx_per_chunk);".format(fn=fn_basic, dt=datatype)
                    )

                    for csc in CSC_TYPES:
                        cscshort = csc["cscshort"]
                        cscdatatype = csc["cscdatatype"]
                        sumtype = csc["sumtype"]
                        fn_csc = make_csc_fn_name(prefix, dtshort, cscshort,
                                                  bkd_suffix, isa_suffix)
                        csc_label = cscshort if cscshort else "f32"

                        lines.append("")
                        lines.append("/* {} CSC {} */".format(dtshort, csc_label))
                        lines.append(
                            "int {fn}(const uint8_t *restrict compressed, "
                            "int compressed_length, "
                            "const uint8_t *restrict mask, int NIJ, "
                            "{dt} *restrict outpx, uint32_t *restrict output_adr, "
                            "int threshold, int encoding, "
                             "{sum} *restrict output, int nout_total, "
                             "{cscdt} *restrict data, uint32_t *restrict indices, "
                             "uint32_t *restrict indptr, "
                            "const int64_t *restrict chunk_offsets, "
                            "const int32_t *restrict chunk_lengths, "
                            "int nchunks, "
                            "int32_t *restrict npx_per_chunk);".format(
                                fn=fn_csc, dt=datatype, sum=sumtype,
                                cscdt=cscdatatype)
                        )

                        fn_csc1d = make_csc1d_fn_name(prefix, dtshort, cscshort,
                                                      bkd_suffix, isa_suffix)
                        lines.append("")
                        lines.append("/* {} CSC1D {} */".format(dtshort, csc_label))
                        lines.append(
                            "int {fn}(const uint8_t *restrict compressed, "
                            "int compressed_length, "
                            "const uint8_t *restrict mask, int NIJ, "
                            "{dt} *restrict outpx, uint32_t *restrict output_adr, "
                            "int threshold, int encoding, "
                             "{sum} *restrict output, int nout_total, "
                             "const {cscdt} *restrict csc_flat, "
                            "const uint32_t *restrict csc_first_bin, "
                            "int csc_entries_per_pixel, "
                            "const int64_t *restrict chunk_offsets, "
                            "const int32_t *restrict chunk_lengths, "
                            "int nchunks, "
                            "int32_t *restrict npx_per_chunk);".format(
                                fn=fn_csc1d, dt=datatype, sum=sumtype,
                                cscdt=cscdatatype)
                        )

    lines.append("")
    lines.append("#endif /* BS_FUNCTIONS_H */")
    return "\n".join(lines) + "\n"


