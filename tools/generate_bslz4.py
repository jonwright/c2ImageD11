def main():
    parser = argparse.ArgumentParser(
        description="Generate bslz4 C boilerplate and c2py23 definitions")
    parser.add_argument("--check", action="store_true",
                        help="Check generated output matches existing files")
    args = parser.parse_args()

    for relpath, generator in sorted(OUTPUTS.items()):
        outpath = os.path.join(REPO, relpath)
        content = generator()

        if args.check:
            if not os.path.exists(outpath):
                print("MISSING: {}".format(relpath))
                sys.exit(1)
            with open(outpath, "r") as f:
                existing = f.read()
            if existing != content:
                print("MISMATCH: {} (re-run generate_bslz4.py)".format(relpath))
                sys.exit(1)
            print("OK: {}".format(relpath))
        else:
            with open(outpath, "w") as f:
                f.write(content)
            print("WROTE: {} ({:,d} bytes)".format(relpath, len(content)))


if __name__ == "__main__":
    main()
