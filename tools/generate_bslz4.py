#!/usr/bin/env python
"""generate_bslz4.py - Generate C boilerplate and c2py23 interface definitions
for the bslz4/bszstd bitshuffle sparse decompression functions.

This script produces three output files from a single configuration table:

  src/bs_master.c        -- master compilation unit with template #includes
  src/bs_functions.h     -- forward declarations for all C functions
  _cImageD11_bslz4.c2py  -- c2py23 function definitions (setup.py splices
                            this into _cImageD11_base.c2py at build time)

Why a generator?
  The function count is combinatorial: 3 pixel types (u8/u16/u32)
  x 4 CSC data types (f32/u8/u16/u32) x 2 engines (LZ4/ZSTD)
  x 2 backends (KCB/bitshuffle-core) x 3 ISAs (SSE42/AVX2/AVX-512).
  That is 144 CSC functions plus 36 basic decompress functions.
  Writing these by hand is error-prone; the C preprocessor template
  expansion and the c2py23 type-check boilerplate follow exact
  mechanical patterns that a generator trivially handles.

Usage:
  python generate_bslz4.py          # writes all three files
  python generate_bslz4.py --check  # diff against current files (for CI)

Python 2.7 compatible (no f-strings, no type annotations).
"""

from __future__ import print_function
import os
import sys
import argparse

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

# ---------------------------------------------------------------------------
# Configuration tables
# ---------------------------------------------------------------------------

# Pixel types unpacked from compressed bitshuffle data
PIXEL_TYPES = [
    {"datatype": "uint8_t",  "dtshort": "u8",  "nb": 1,
     "py_format": "B", "c2py_format": "B"},
    {"datatype": "uint16_t", "dtshort": "u16", "nb": 2,
     "py_format": "H", "c2py_format": "H"},
    {"datatype": "uint32_t", "dtshort": "u32", "nb": 4,
     "py_format": "I", "c2py_format": "I"},
]

# CSC matrix data types: float (legacy) + integer (new exact-arithmetic)
# cscshort determines the suffix appended to function names.
# For float: suffix is empty (backward-compatible naming, e.g. bslz4_csc_u16_...)
# For integer: suffix like "cu16" -> function name bslz4_csc_u16_cu16_...
CSC_TYPES = [
    {"cscdatatype": "float",    "cscshort": "",
     "cformat": "f", "sumtype": "double",   "sformat": "d",
     "is_float": True},
]

ENGINES = [
    {"suffix": "",  "prefix": "bs",  "extra_flags": ""},
]

BACKENDS = [
    {"suffix": "kcb", "extra_flags": "-DUSE_KCB"},
]

ISAS = [
    {"suffix": "avx512", "isa_flag": "-mavx512f",
     "when_cond": "c2py_amd64_avx512f", "is_default": False},
    {"suffix": "avx2",   "isa_flag": "-mavx2",
     "when_cond": "c2py_amd64_avx2", "is_default": False},
    {"suffix": "sse42",  "isa_flag": "-msse4.2",
     "when_cond": None, "is_default": True},
]

# ---------------------------------------------------------------------------
# Naming helpers
# ---------------------------------------------------------------------------

def make_basic_fn_name(prefix, dtshort, backend, isa):
    """e.g. bslz4_u16_kcb_avx512"""
    return "{}_{}_{}_{}".format(prefix, dtshort, backend, isa)

def make_csc_fn_name(prefix, dtshort, cscshort, backend, isa):
    """e.g. bslz4_csc_u16_cu16_kcb_avx512 (int) or bslz4_csc_u16_kcb_avx512 (float)"""
    if cscshort:
        return "{}_csc_{}_{}_{}_{}".format(prefix, dtshort, cscshort, backend, isa)
    else:
        return "{}_csc_{}_{}_{}".format(prefix, dtshort, backend, isa)

def make_py_fn_name(prefix, dtshort, cscshort):
    """Python-visible function name. e.g. bslz4_csc_u16_cu16"""
    if cscshort:
        return "{}_csc_{}_{}".format(prefix, dtshort, cscshort)
    else:
        return "{}_csc_{}".format(prefix, dtshort)

def make_csc1d_fn_name(prefix, dtshort, cscshort, backend, isa):
    """e.g. bslz4_csc1d_u16_cu16_kcb_avx512 (int) or bslz4_csc1d_u16_kcb_avx512 (float)"""
    if cscshort:
        return "{}_csc1d_{}_{}_{}_{}".format(prefix, dtshort, cscshort, backend, isa)
    else:
        return "{}_csc1d_{}_{}_{}".format(prefix, dtshort, backend, isa)

def make_py_csc1d_fn_name(prefix, dtshort, cscshort):
    """Python-visible 1D CSC function name. e.g. bslz4_csc1d_u16_cu16"""
    if cscshort:
        return "{}_csc1d_{}_{}".format(prefix, dtshort, cscshort)
    else:
        return "{}_csc1d_{}".format(prefix, dtshort)

def make_kernel_suffix(backend, isa):
    """What gets passed via -DKERNEL_SUFFIX to cc"""
    return "_{}_{}".format(backend, isa)

def make_full_suffix(backend, isa):
    """Full suffix used in object file names"""
    return "_{}_{}".format(backend, isa)

