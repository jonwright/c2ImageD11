class TestF2pyEquivalence:
    """Compare c2ImageD11 output against original f2py extension."""

    def test_lz4_u16_c_layer(self):
        """Compare bslz4_u16 C function directly with the f2py version."""
        try:
            from bslz4_to_sparse import bslz4_uint16_t as f2py_fn
        except ImportError:
            pytest.skip("original bslz4_to_sparse not installed")

        h5py = pytest.importorskip("h5py")
        hdf5plugin = pytest.importorskip("hdf5plugin")  # noqa

        # Use the eiger data if available
        SRC = "/home/worker/test_data/eiger_lz4_u16.h5"
        if not os.path.exists(SRC):
            SRC = "/home/worker/test_data/eiger_0000.h5"
        if not os.path.exists(SRC):
            pytest.skip("no test data file found")

        with h5py.File(SRC, "r") as f:
            ds = f["/entry_0000/measurement/data"]
            mask = np.ones(ds.shape[1:], dtype=np.uint8)
            N = mask.size
            vals1 = np.empty(N, dtype=np.uint16)
            inds1 = np.empty(N, dtype=np.uint32)
            vals2 = np.empty(N, dtype=np.uint16)
            inds2 = np.empty(N, dtype=np.uint32)

            nframes = min(ds.shape[0], 10)
            for i in range(nframes):
                _, chunk = ds.id.read_direct_chunk((i, 0, 0))

                npx1 = f2py_fn(np.frombuffer(chunk, np.uint8), mask.ravel(), vals1, inds1, 0)
                npx2 = _m.bslz4_u16(chunk, mask.ravel(), vals2, inds2, 0)

                assert npx1 == npx2, f"frame {i}: npx mismatch"
                np.testing.assert_array_equal(vals1[:npx1], vals2[:npx2],
                    err_msg=f"frame {i}: values mismatch")
                np.testing.assert_array_equal(inds1[:npx1], inds2[:npx2],
                    err_msg=f"frame {i}: indices mismatch")

    def test_lz4_csc_u16_c_layer(self):
        """Compare bslz4_csc_u16 with original f2py version."""
        try:
            from bslz4_to_sparse import bslz4_csc_uint16_t as f2py_fn
        except ImportError:
            pytest.skip("original bslz4_to_sparse not installed")

        h5py = pytest.importorskip("h5py")
        hdf5plugin = pytest.importorskip("hdf5plugin")  # noqa

        SRC = "/home/worker/test_data/eiger_lz4_u16.h5"
        if not os.path.exists(SRC):
            SRC = "/home/worker/test_data/eiger_0000.h5"
        if not os.path.exists(SRC):
            pytest.skip("no test data file found")

        with h5py.File(SRC, "r") as f:
            ds = f["/entry_0000/measurement/data"]
            mask = np.ones(ds.shape[1:], dtype=np.uint8)
            flat = mask.ravel()
            N = flat.size

            data = np.ones(N, dtype=np.float32)
            indices = np.zeros(N, dtype=np.uint32)
            indptr = np.arange(N + 1, dtype=np.uint32)
            outpx1 = np.empty(N, dtype=np.uint16)
            outP1 = np.empty(N, dtype=np.uint32)
            powder1 = np.zeros(1, dtype=np.float64)
            outpx2 = np.empty(N, dtype=np.uint16)
            outP2 = np.empty(N, dtype=np.uint32)
            powder2 = np.zeros(1, dtype=np.float64)

            for i in range(min(ds.shape[0], 5)):
                _, chunk = ds.id.read_direct_chunk((i, 0, 0))

                npx1 = f2py_fn(np.frombuffer(chunk, np.uint8), flat, outpx1, outP1, 0,
                               powder1, data, indices, indptr)
                npx2 = _m.bslz4_csc_u16(chunk, flat, outpx2, outP2, 0,
                                        powder2, data, indices, indptr)

                assert npx1 == npx2, f"frame {i}: npx mismatch"
                np.testing.assert_array_equal(outpx1[:npx1], outpx2[:npx2])
                np.testing.assert_array_equal(outP1[:npx1], outP2[:npx2])
                np.testing.assert_allclose(powder1, powder2, rtol=1e-10)
