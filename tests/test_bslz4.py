@pytest.mark.skipif(
    not os.path.exists(TESTDATA_LZ4),
    reason="run 'python3 tests/make_bs_testdata.py --zstd' first"
)
class TestBitPerfectLZ4:
    """Compare bslz4_* output against pure-numpy reference for LZ4 data."""

    ENGINES = {
        "lz4": {"basic": {np.uint8: _m.bslz4_u8, np.uint16: _m.bslz4_u16, np.uint32: _m.bslz4_u32},
                "csc":   {np.uint8: _m.bslz4_csc_u8, np.uint16: _m.bslz4_csc_u16, np.uint32: _m.bslz4_csc_u32}},
    }

    def _datasets(self):
        h5py = pytest.importorskip("h5py")
        hdf5plugin = pytest.importorskip("hdf5plugin")  # noqa
        with h5py.File(TESTDATA_LZ4, "r") as f:
            return [(name, f[name].shape, f[name].dtype) for name in sorted(f.keys())]

    def _test_dataset(self, ds_info, engine, cut=0):
        name, shape, dtype = ds_info
        dtype = np.dtype(dtype).type
        h5py = pytest.importorskip("h5py")
        mask = np.ones(shape[1:], dtype=np.uint8)
        nframes = min(shape[0], 4)

        func = self.ENGINES[engine]["basic"][dtype]
        N = mask.size
        vals = np.empty(N, dtype=dtype)
        inds = np.empty(N, dtype=np.uint32)

        with h5py.File(TESTDATA_LZ4, "r") as f:
            ds = f[name]
            for frame in range(nframes):
                ref_vals, ref_inds = _pysparse(ds[frame], mask, cut)
                chunk_info, chunk = ds.id.read_direct_chunk((frame, 0, 0))
                npx = func(chunk, mask.ravel(), vals, inds, cut)
                assert npx == len(ref_vals), \
                    f"npx mismatch: {npx} vs {len(ref_vals)} at frame={frame} cut={cut}"
                np.testing.assert_array_equal(vals[:npx], ref_vals,
                    err_msg=f"values mismatch at frame={frame} cut={cut}")
                np.testing.assert_array_equal(inds[:npx], ref_inds,
                    err_msg=f"indices mismatch at frame={frame} cut={cut}")

    def _test_dataset_csc(self, ds_info, engine, cut=0):
        name, shape, dtype = ds_info
        dtype = np.dtype(dtype).type
        h5py = pytest.importorskip("h5py")
        mask = np.ones(shape[1:], dtype=np.uint8)
        flat = mask.ravel()
        func = self.ENGINES[engine]["csc"][dtype]
        N = flat.size

        data = np.ones(N, dtype=np.float32)
        indices = np.zeros(N, dtype=np.uint32)
        indptr = np.arange(N + 1, dtype=np.uint32)
        outpx = np.empty(N, dtype=dtype)
        outP = np.empty(N, dtype=np.uint32)
        powder = np.zeros(1, dtype=np.float64)
        powder_ref = np.zeros(1, dtype=np.float64)

        with h5py.File(TESTDATA_LZ4, "r") as f:
            ds = f[name]
            for frame in range(min(shape[0], 2)):
                ref_vals, ref_inds = _pysparse_csc(ds[frame], mask, cut,
                                                   data, indices, indptr, powder_ref)
                chunk_info, chunk = ds.id.read_direct_chunk((frame, 0, 0))
                npx = func(chunk, flat, outpx, outP, cut,
                          powder, data, indices, indptr)
                assert npx == len(ref_vals), f"CSC npx mismatch frame={frame}"
                np.testing.assert_array_equal(outpx[:npx], ref_vals,
                    err_msg=f"CSC values mismatch frame={frame}")
                np.testing.assert_array_equal(outP[:npx], ref_inds,
                    err_msg=f"CSC indices mismatch frame={frame}")
                np.testing.assert_allclose(powder, powder_ref, rtol=1e-10,
                    err_msg=f"CSC powder mismatch frame={frame}")

    def test_lz4_basic_all_datasets(self):
        for ds_info in self._datasets():
            self._test_dataset(ds_info, "lz4", 0)
            self._test_dataset(ds_info, "lz4", 10)

    def test_lz4_csc_all_datasets(self):
        for ds_info in self._datasets():
            self._test_dataset_csc(ds_info, "lz4", 0)
            self._test_dataset_csc(ds_info, "lz4", 10)


